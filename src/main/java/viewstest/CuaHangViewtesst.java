    public void loadTB(){
        model = (DefaultTableModel) tbCH.getModel();
        model.setColumnCount(0);
        model.addColumn(txtid.getText());
        model.addColumn(txtma.getText());
        model.addColumn(txtTen.getText());
        model.addColumn(txtdiachi.getText());
        
        model.setRowCount(0);
        
        List<ViewModelCuaHangtesst> ch = cuaHangServicetesst.getAll();
        
        for (ViewModelCuaHangtesst viewModelCuaHangtesst : ch) {
            model.addRow(new Object[]{
                viewModelCuaHangtesst.getId(),
                viewModelCuaHangtesst.getMa(),
                viewModelCuaHangtesst.getTenCuaHang(),
                viewModelCuaHangtesst.getDiaChi()
            });
        }
    }
    public void loadTimkiem(String ten){
        model = (DefaultTableModel) tbCH.getModel();
        model.setColumnCount(0);
        model.addColumn(txtid.getText());
        model.addColumn(txtma.getText());
        model.addColumn(txtTen.getText());
        model.addColumn(txtdiachi.getText());
        
        model.setRowCount(0);
        
        List<ViewModelCuaHangtesst> ch = cuaHangServicetesst.getListSPByName(ten);
        
        for (ViewModelCuaHangtesst viewModelCuaHangtesst : ch) {
            model.addRow(new Object[]{
                viewModelCuaHangtesst.getId(),
                viewModelCuaHangtesst.getMa(),
                viewModelCuaHangtesst.getTenCuaHang(),
                viewModelCuaHangtesst.getDiaChi()
            });
        }
    }
    
     public void loadPhanTu1() {
        List<ViewModelCuaHangtesst> ch = cuaHangServicetesst.getAll();
        if (ch.isEmpty()) {
            return;
        }
        int index = 0;

        txtid.setText(tbCH.getValueAt(index, 0).toString());
        txtma.setText(tbCH.getValueAt(index, 1).toString());
        txtTen.setText(tbCH.getValueAt(index, 2).toString());
        txtdiachi.setText(tbCH.getValueAt(index, 3).toString());
    }
      
      
       public boolean checkTen() { //check ten sp
        String ten = txtTen.getText();
        List<ViewModelCuaHangtesst> ch = cuaHangServicetesst.getListSPByName(ten);
        for (ViewModelCuaHangtesst v : ch) {
            if (ten.equals(v.getTenCuaHang())) {
                JOptionPane.showMessageDialog(this, "Tên đã tồn tại!");
                return false;
            }
        }
        return true;
    }
