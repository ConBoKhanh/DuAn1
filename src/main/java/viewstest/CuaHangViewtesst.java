    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        CuaHang ch =  new CuaHang();
        ch.setId(txtid.getText());
        ch.setTenCuaHang(txtTen.getText());
        ch.setDiaChi(txtdiachi.getText());
        
          if (checkTen()) {
            boolean b = cuaHangServicetesst.add(ch);
            if (b == true) {
                Icon icon = new javax.swing.ImageIcon(getClass().getResource("/img/themmoiicon.png"));
                JOptionPane.showMessageDialog(this, "them sp thành công", "Sản Phẩm", JOptionPane.INFORMATION_MESSAGE, icon);
               loadTB();

            } else {
                Icon icon = new javax.swing.ImageIcon(getClass().getResource("/img/deleteicon.png"));
                JOptionPane.showMessageDialog(this, "them SP thất bại", "Sản Phẩm", JOptionPane.INFORMATION_MESSAGE, icon);
            }
        }
    }//GEN-LAST:event_btnThemActionPerformed

